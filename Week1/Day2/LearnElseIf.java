package week1.day2;

public class LearnElseIf {

	public static void main(String[] args) {
		int number = 0;

		if (number > 0) {
			System.out.println("The number is Positive");
		}
        else if (number < 0) {
			System.out.println("The number is Negative");
		} else {
			System.out.println("The number is Zero");
		}

	}

}
